function update(jscolor, part) {
  let color = ("#" + jscolor).toUpperCase();
  $("#" + part).html(color);
  if (part == 'fieldBgDefault' && document.getElementById('bgtoall').checked) {
    $("[id^='fieldBg']").html(color);
    $(".bg").each(function() { this.jscolor.fromString(color); });
  }
  document.getElementById('bgtoall').checked = false;
}

